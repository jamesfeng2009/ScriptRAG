def save_documents():
    """保存所有文档"""
    output_dir = Path(__file__).parent.parent / "data" / "knowledge" / "saint_seiya"
    output_dir.mkdir(parents=True, exist_ok=True)
    
    # 生成所有文档
    all_documents = generate_all_documents()
    
    # 保存为JSON文件
    output_file = output_dir / "saint_seiya_complete_knowledge.json"
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(all_documents, f, ensure_ascii=False, indent=2)
    
    print(f"已保存 {len(all_documents)} 条文档到 {output_file}")
    
    # 统计各类文档数量
    categories = {}
    for doc in all_documents:
        cat = doc.get('metadata', {}).get('category', 'unknown')
        categories[cat] = categories.get(cat, 0) + 1
    
    print("\n文档统计：")
    for cat, count in categories.items():
        print(f"  - {cat}: {count} 条")
    
    return all_documents


